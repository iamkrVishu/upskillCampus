import mongoose from "mongoose";

const userSchema = mongoose.Schema({
    fullname: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    },
});

// Use mongoose.models to check if the model is already defined
const User = mongoose.models.User || mongoose.model("User", userSchema);

export default User;
